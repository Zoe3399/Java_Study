public class Ex21Method {
    public static void main(String[] args) {
        
        // Method (기능) : Python 함수와 동일!
        System.out.println(sum(5, 10));
        System.out.println(sum(10, 20));
        System.out.println(sum(100, 200));
        
    }

    /*Method(함수) 정의*/
    public static int sum(int a, int b){
        return a + b;
    }
}
